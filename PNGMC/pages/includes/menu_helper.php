// Function to output mobile menu JavaScript
function getMobileMenuScript() {
    return '<script>
        function toggleMobileMenu() {
            const sidebar = document.querySelector(".sidebar");
            const overlay = document.querySelector(".sidebar-overlay");
            const toggle = document.getElementById("menuToggle");
            
            if (sidebar && overlay && toggle) {
                sidebar.classList.toggle("open");
                overlay.classList.toggle("active");
                
                // Change icon
                if (sidebar.classList.contains("open")) {
                    toggle.innerHTML = \'<svg viewBox="0 0 24 24" width="24" height="24"><path d="M19 6.41L17.59 5 12 10.59 6.41 5 5 6.41 10.59 12 5 17.59 6.41 19 12 13.41 17.59 19 19 17.59 13.41 12z"/></svg>\';
                } else {
                    toggle.innerHTML = \'<svg viewBox="0 0 24 24" width="24" height="24"><path d="M3 18h18v-2H3v2zm0-5h18v-2H3v2zm0-7v2h18V6H3z"/></svg>\';
                }
            }
        }
        
        // Close menu when clicking overlay
        document.addEventListener("DOMContentLoaded", function() {
            const overlay = document.querySelector(".sidebar-overlay");
            if (overlay) {
                overlay.addEventListener("click", function() {
                    toggleMobileMenu();
                });
            }
            
            // Close menu when clicking a menu item on mobile
            const menuItems = document.querySelectorAll(".menu-item");
            menuItems.forEach(function(item) {
                item.addEventListener("click", function() {
                    if (window.innerWidth <= 900) {
                        setTimeout(function() {
                            toggleMobileMenu();
                        }, 100);
                    }
                });
            });
        });
    </script>';
}

